public class tiposevariaveis {
    public static void main(String[] args) throws Exception {
        double a = 10.5;
        short b = 5;
        int c = 7;
        double d = a + b + c;
        System.out.println("The result is: " + d);
        byte e = 1;
        char f = 'A';
        float g = 3.14f;
        long h = 100000L;
        boolean i = true;
        String j = "Hello, World!";

        System.out.println("byte e: " + e);
        System.out.println("char f: " + f);
        System.out.println("float g: " + g);
        System.out.println("long h: " + h);
        System.out.println("boolean i: " + i);
        System.out.println("String j: " + j);
    }
}
